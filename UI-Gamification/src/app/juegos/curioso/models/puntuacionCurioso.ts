export class puntuacionCurioso{
    nombre:string
    usuario:string
    codigo_aula:string
    puntuacion:number
    total_aciertos:number

    constructor(nombre:string,usuario:string,codigo_aula:string,puntuacion:number,total_aciertos:number){
        this.nombre=nombre,
        this.usuario=usuario;
        this.codigo_aula=codigo_aula;
        this.puntuacion=puntuacion;
        this.total_aciertos=total_aciertos;
    }




}